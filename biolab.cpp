#include <iostream>

using namespace std;

int main() {
    int bacteria, drops;

    // Получение ввода от пользователя
    cout << "Сколько бактерий вы хотите поместить в чашку Петри?: ";
    cin >> bacteria;

    // Проверка корректности ввода для количества бактерий
    if (bacteria < 0) {
        cout << "Количество бактерий не может быть отрицательным." << endl;
        return 1; // Завершение программы с ошибкой
    }

    cout << "Сколько капель антибиотика вы хотите добавить?: ";
    cin >> drops;

    // Проверка корректности ввода для количества антибиотика
    if (drops < 0) {
        cout << "Количество антибиотика не может быть отрицательным." << endl;
        return 1; // Завершение программы с ошибкой
    }

    for (int hour = 1; hour <= 10 && bacteria > 0; hour++) {
        // Увеличение количества бактерий
        bacteria *= 2;

        // Рассчет количества убитых бактерий в текущем часу
        int killed = max(0, 10 - (hour - 1)); // Убивает 10, 9, 8 и т.д. до нуля
        int bacteriaKilled = killed * drops; // Общее количество убитых бактерий

        // Уменьшение количества бактерий на количество убитых
        bacteria -= bacteriaKilled;

        // Если количество бактерий становится меньше нуля, устанавливаем его в ноль
        if (bacteria < 0) bacteria = 0;

        // Определение правильной формы слова "час"
        string hourWord;
        if (hour == 1) {
            hourWord = "час";
            cout << "Прошел " << hour << " " << hourWord << ", живых бактерий осталось: " << bacteria << endl;
        } else {
            hourWord = (hour >= 2 && hour <= 4) ? "часа" : "часов";
            cout << "Прошло " << hour << " " << hourWord << ", живых бактерий осталось: " << bacteria << endl;
        }
    }

    // Вывод окончательного результата
    cout << "Эксперимент окончен, выжило бактерий: " << bacteria << endl;

    return 0;
}
